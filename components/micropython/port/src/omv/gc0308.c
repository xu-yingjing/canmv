static int gc0308_set_colorbar(sensor_t *sensor, int enable)
{
    uint8_t reg_data;

    cambus_writeb(GC0308_ADDR, 0xFE, 0x00);
    cambus_readb(GC0308_ADDR, 0x2E, &reg_data);
    reg_data &= ~(0x01 << 0);
    if (enable)
    {
        reg_data |= (0x01 << 0);
    }
    cambus_writeb(GC0308_ADDR, 0x2E, reg_data);
    return 0;
}

